    def export_to_json(self, output_path: str = None) -> str:
        """
        导出所有节点数据到JSON文件
        
        Args:
            output_path: 输出文件路径，默认保存到 data/wencui_rooms_generated.json
            
        Returns:
            输出文件路径
        """
        if output_path is None:
            output_path = os.path.join(
                os.path.dirname(__file__), '..', 'data', 'wencui_rooms_generated.json'
            )
        
        # 生成房间节点
        room_nodes = self.generate_room_nodes()
        
        # 生成额外节点
        additional_nodes = self.generate_additional_nodes()
        
        # 合并所有节点
        all_nodes = {**room_nodes, **additional_nodes}
        
        # 构建输出数据
        data = {
            "building": {
                "name": "文萃楼",
                "description": "北京理工大学良乡校区文萃楼 — 13个分区(A-M)构成的U形教学科研楼宇群",
                "floors": self.floors
            },
            "metadata": {
                "total_nodes": len(all_nodes),
                "room_count": len(room_nodes),
                "stairs_count": len([n for n in additional_nodes.values() if n['type'] == 'stairs']),
                "elevator_count": len([n for n in additional_nodes.values() if n['type'] == 'elevator']),
                "zones": list(self.zone_configs.keys()),
                "coordinate_system": {
                    "origin": "top-left",
                    "unit": "pixel",
                    "reference_image": "1F floorplan"
                }
            },
            "nodes": all_nodes
        }
        
        # 保存到文件
        os.makedirs(os.path.dirname(output_path), exist_ok=True)
        with open(output_path, 'w', encoding='utf-8') as f:
            json.dump(data, f, ensure_ascii=False, indent=2)
        
        print(f"已生成 {len(room_nodes)} 个房间节点")
        print(f"已生成 {len(additional_nodes)} 个设施节点（楼梯/电梯）")
        print(f"总计 {len(all_nodes)} 个节点")
        print(f"数据已保存到: {output_path}")
        
        return output_path


