    def generate_room_nodes(self) -> Dict:
        """
        生成所有房间节点数据
        
        Returns:
            节点数据字典，格式与 wencui_building.json 一致
        """
        nodes = {}
        
        for zone, floor_config in self.zone_configs.items():
            for floor, rooms in floor_config.items():
                for room_suffix, x, y in rooms:
                    # 构建房间ID：{区号}-{楼层}{序号}
                    # 例如：C-101 表示 C区 1楼 01号
                    floor_num = floor.replace('F', '')
                    room_id = f"{zone}-{floor_num}{room_suffix}"
                    
                    nodes[room_id] = {
                        "id": room_id,
                        "type": "room",
                        "floor": floor,
                        "zone": zone,
                        "name": room_id,
                        "description": f"{floor} {zone}区房间",
                        "x": x,
                        "y": y
                    }
        
        return nodes
    
